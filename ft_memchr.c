#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*a;

	if (n <= 0)
		return (0);
	a = (void *) s;
	while (n > 0)
	{
		if (*a == (char)c)
			return (a);
		a++;
		n--;
	}
	return (0);
}
