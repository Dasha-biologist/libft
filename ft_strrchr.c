char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s != '\0')
	{
		s++;
		i++;
	}
	while (*s != (char)c && i > 0)
	{
		s--;
		i--;
	}
	if (*s != (char)c)
		return (0);
	else
		return ((char *)s);
}
